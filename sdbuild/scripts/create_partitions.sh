#! /bin/bash

# fdisk is in /sbin which isn't necessarily on the PATH
export PATH=/sbin:$PATH

# Sourced from http://superuser.com/questions/332252/creating-and-formating-a-partition-using-a-bash-script

# to create the partitions programatically (rather than manually)
# we're going to simulate the manual input to fdisk
# The sed script strips off all the comments so that we can 
# document what we're doing in-line with the actual commands
# Note that a blank line (commented as "defualt" will send a empty
# line terminated with a newline to take the fdisk default.
sed -e 's/\s*\([\+0-9a-zA-Z]*\).*/\1/' << EOF | fdisk $1
  o # clear the in memory partition table
  n # new partition
  p # primary partition
  1 # partition number 1
    # default - start at beginning of disk 
  +100M # 100 MB boot parttion
  n # new partition
  p # primary partition
  2 # partion number 2
    # default, start immediately after preceding partition
    # default, extend partition to end of disk
  a # make a partition bootable
  1 # bootable partition is partition 1 -- /dev/sda1
  t # Change the type of the partition
  1 # Boot partition
  c # FAT 32
  p # print the in-memory partition table
  w # write the partition table
  q # and we're done
EOF
